    if input_price:
        input_price=input_price.replace(' ','').replace(',','.') #zarez separator
        input_price=regexp(r"\d+\.?\d*",input_price)
        if input_price > "":
            return float(input_price)
        else:
            return 0.0
    else:
        return 0.0

